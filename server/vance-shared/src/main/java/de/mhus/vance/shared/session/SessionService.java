    /**
     * Applies a {@link SessionMetadataPatchRequest} to the session,
     * normalising tag input and stamping {@code userTouchedAt} on the
     * first user-driven change. If {@code title} is in the patch, the
     * {@code titleAutoGenerated} flag flips to {@code false} so the
     * LLM auto-suggester never overwrites the user's edit later.
     *
     * <p>Returns {@code true} on at least one persisted field change.
     */
    /**
     * Persist the Cortex view state on a session. The Cortex client
     * calls this whenever the user opens/closes a tab or binds the
     * chat to a different document. Both fields are replaced wholesale
     * (no per-element semantics). {@code chatBoundDocumentId} is
     * unset when {@code null} is passed; an empty {@code openDocumentIds}
     * list collapses all tabs.
     *
     * <p>Does not touch {@code userTouchedAt} — Cortex layout edits
     * are not "the user invested in the session content" signals; they
     * are pure UI restoration state. See planning/cortex.md §4.1.
     */
    public boolean setCortexState(
            String sessionId,
            List<String> openDocumentIds,
            @Nullable String chatBoundDocumentId) {
        Update update = new Update();
        update.set(F_OPEN_DOCUMENT_IDS,
                openDocumentIds == null ? List.of() : List.copyOf(openDocumentIds));
        if (chatBoundDocumentId == null || chatBoundDocumentId.isBlank()) {
            update.unset(F_CHAT_BOUND_DOCUMENT_ID);
        } else {
            update.set(F_CHAT_BOUND_DOCUMENT_ID, chatBoundDocumentId);
        }
        UpdateResult result = mongoTemplate.updateFirst(
                new Query(Criteria.where(F_SESSION_ID).is(sessionId)),
                update, SessionDocument.class);
        return result.getModifiedCount() == 1;
    }

    public boolean patchMetadata(String sessionId, SessionMetadataPatchRequest patch) {
        if (patch == null) return false;
        Update update = new Update();
        boolean changed = false;

        if (patch.getTitle() != null) {
            String title = patch.getTitle().trim();
            if (title.isEmpty()) {
                update.unset(F_TITLE);
            } else {
                if (title.length() > TITLE_MAX_CHARS) {
                    title = title.substring(0, TITLE_MAX_CHARS);
                }
                update.set(F_TITLE, title);
            }
            update.set(F_TITLE_AUTO, false);
            changed = true;
        }
        if (patch.getIcon() != null) {
            String icon = patch.getIcon();
            if (icon.length() > ICON_MAX_CHARS) {
                icon = icon.substring(0, ICON_MAX_CHARS);
            }
            if (icon.isEmpty()) {
                update.unset(F_ICON);
            } else {
                update.set(F_ICON, icon);
            }
            changed = true;
        }
        if (patch.getColor() != null) {
            update.set(F_COLOR, patch.getColor());
            changed = true;
        }
        if (patch.getTags() != null) {
            update.set(F_TAGS, normalizeTags(patch.getTags()));
            changed = true;
        }
        if (patch.getPinned() != null) {
            update.set(F_PINNED, patch.getPinned());
            changed = true;
        }

        if (!changed) return false;

        update.set(F_USER_TOUCHED_AT, Instant.now());
        UpdateResult result = mongoTemplate.updateFirst(
                new Query(Criteria.where(F_SESSION_ID).is(sessionId)),
                update, SessionDocument.class);
        if (result.getModifiedCount() == 1) {
            log.info("Patched metadata on session '{}'", sessionId);
            return true;
        }
        return false;
    }

    /**
     * Applies the LLM auto-suggester output to empty metadata fields
     * only — user-edited values are never overwritten. Sets
     * {@code titleAutoGenerated=true} when the title is filled here.
     * Does <em>not</em> stamp {@code userTouchedAt}: the suggester is
     * a system signal, not a user signal.
     *
     * <p>The Mongo query enforces the "only fill when empty" rule via
     * {@code $exists:false}/{@code $eq:null} clauses, so a racing
     * patch from the user wins.
     */
    public void applyAutoSuggestedMetadata(
            String sessionId,
            @Nullable String suggestedTitle,
            @Nullable String suggestedIcon,
            @Nullable AccentColor suggestedColor) {
        if (suggestedTitle != null && !suggestedTitle.isBlank()) {
            String title = suggestedTitle.trim();
            if (title.length() > TITLE_MAX_CHARS) title = title.substring(0, TITLE_MAX_CHARS);
            Query q = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                    .andOperator(new Criteria().orOperator(
                            Criteria.where(F_TITLE).isNull(),
                            Criteria.where(F_TITLE).exists(false))));
            Update u = new Update().set(F_TITLE, title).set(F_TITLE_AUTO, true);
            mongoTemplate.updateFirst(q, u, SessionDocument.class);
        }
        if (suggestedIcon != null && !suggestedIcon.isBlank()) {
            String icon = suggestedIcon;
            if (icon.length() > ICON_MAX_CHARS) icon = icon.substring(0, ICON_MAX_CHARS);
            Query q = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                    .andOperator(new Criteria().orOperator(
                            Criteria.where(F_ICON).isNull(),
                            Criteria.where(F_ICON).exists(false))));
            Update u = new Update().set(F_ICON, icon);
            mongoTemplate.updateFirst(q, u, SessionDocument.class);
        }
        if (suggestedColor != null) {
            Query q = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                    .andOperator(new Criteria().orOperator(
                            Criteria.where(F_COLOR).isNull(),
                            Criteria.where(F_COLOR).exists(false))));
            Update u = new Update().set(F_COLOR, suggestedColor);
            mongoTemplate.updateFirst(q, u, SessionDocument.class);
        }
        log.debug("Auto-suggested metadata applied to session '{}'", sessionId);
    }

    private static List<String> normalizeTags(List<String> raw) {
        LinkedHashSet<String> normalised = new LinkedHashSet<>();
        for (String t : raw) {
            if (t == null) continue;
            String n = t.trim().toLowerCase(Locale.ROOT);
            if (n.isEmpty()) continue;
            if (n.length() > TAG_MAX_CHARS) n = n.substring(0, TAG_MAX_CHARS);
            normalised.add(n);
            if (normalised.size() >= MAX_TAGS) break;
        }
        return new ArrayList<>(normalised);
    }

