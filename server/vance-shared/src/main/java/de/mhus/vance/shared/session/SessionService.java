    public SessionDocument create(
            String tenantId,
            String userId,
            String projectId,
            @Nullable String displayName,
            String profile,
            String clientVersion,
            @Nullable String clientName) {
        return create(tenantId, userId, projectId, displayName, profile,
                clientVersion, clientName, false);
    }

    /**
     * Variant that allows marking a session as {@link SessionDocument#isSystem()
     * system-owned} (e.g. scheduler-created sessions — see
     * {@code specification/scheduler.md} §6). System sessions are
     * persisted with the same lifecycle defaults as user sessions; the
     * flag is purely advisory for UI filtering and metadata behaviour.
     */
    public SessionDocument create(
            String tenantId,
            String userId,
            String projectId,
            @Nullable String displayName,
            String profile,
            String clientVersion,
            @Nullable String clientName,
            boolean system) {
        Instant now = Instant.now();
        SessionLifecycleConfig defaults = SessionLifecycleConfig.safeDefault();
        SessionDocument doc = SessionDocument.builder()
                .sessionId(newSessionId())
                .tenantId(tenantId)
                .userId(userId)
                .projectId(projectId)
                .displayName(displayName)
                .profile(profile)
                .clientVersion(clientVersion)
                .clientName(clientName)
                .boundConnectionId(null)
                .status(SessionStatus.INIT)
                .onDisconnect(defaults.getOnDisconnect())
                .onIdle(defaults.getOnIdle())
                .onSuspend(defaults.getOnSuspend())
                .idleTimeoutMs(defaults.getIdleTimeoutMs())
                .suspendKeepDurationMs(defaults.getSuspendKeepDurationMs())
                .createdAt(now)
                .lastActivityAt(now)
                .system(system)
                .build();
        SessionDocument saved = repository.save(doc);
        log.info("Created session sessionId='{}' tenant='{}' user='{}' project='{}' system={}",
                saved.getSessionId(), saved.getTenantId(), saved.getUserId(),
                saved.getProjectId(), system);
        return saved;
    }

    /**
     * Replaces the lifecycle config on a session — used by the
     * bootstrap path immediately after recipe resolution. Once
     * persisted, the policy fields are read by the lifecycle code
     * and not changed again for the lifetime of the session.
     */
    public void applyLifecycleConfig(String sessionId, SessionLifecycleConfig cfg) {
        Update update = new Update()
                .set("onDisconnect", cfg.getOnDisconnect())
                .set("onIdle", cfg.getOnIdle())
                .set("onSuspend", cfg.getOnSuspend())
                .set("idleTimeoutMs", cfg.getIdleTimeoutMs())
                .set("suspendKeepDurationMs", cfg.getSuspendKeepDurationMs());
        mongoTemplate.updateFirst(
                new Query(Criteria.where(F_SESSION_ID).is(sessionId)),
                update,
                SessionDocument.class);
    }

    /**
     * Marks a session's bootstrap as complete: status flips from
     * {@link SessionStatus#INIT} to {@link SessionStatus#IDLE}.
     * No-op for sessions that are already past INIT.
     */
    public void markBootstrapped(String sessionId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_STATUS).is(SessionStatus.INIT));
        Update update = new Update().set(F_STATUS, SessionStatus.IDLE);
        mongoTemplate.updateFirst(query, update, SessionDocument.class);
    }

    // --------------------------------------------------------- atomic locks

    /**
     * Atomically claims the connection lock on {@code sessionId}. Succeeds when
     * the session is bindable (status not CLOSED/ARCHIVED) and one of:
     * <ul>
     *   <li>no connection is currently bound, or</li>
     *   <li>the bound connection's last heartbeat (={@code lastActivityAt})
     *       is older than {@link #bindStaleAfter} — the previous owner is
     *       assumed dead and gets taken over.</li>
     * </ul>
     *
     * <p>The stale-takeover branch is what unblocks reconnects after a
     * crashed pod or a TCP-only loss: heartbeats (any inbound frame, ping
     * included) keep the bind alive, no heartbeats for two ping intervals
     * release it. Pod-affinity is tracked elsewhere — see project
     * lifecycle.
     *
     * @return {@code true} if the caller now owns the lock, {@code false} if
     *         the session is closed/archived, missing, or held by another
     *         connection whose heartbeat is still fresh.
     */
    public boolean tryBind(String sessionId, String editorId) {
        Instant now = Instant.now();
        Instant staleBefore = now.minus(bindStaleAfter);
        Criteria base = Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_STATUS).nin(SessionStatus.CLOSED, SessionStatus.ARCHIVED);
        Criteria takeoverPredicate = new Criteria().orOperator(
                Criteria.where(F_BOUND_CONNECTION).isNull(),
                Criteria.where(F_BOUND_CONNECTION).is(editorId),
                Criteria.where(F_LAST_ACTIVITY).lt(staleBefore));
        Query query = new Query(new Criteria().andOperator(base, takeoverPredicate));
        Update update = new Update()
                .set(F_BOUND_CONNECTION, editorId)
                .set(F_LAST_ACTIVITY, now);
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        boolean bound = result.getModifiedCount() == 1;
        if (bound) {
            log.debug("Bound session '{}' to editor '{}'", sessionId, editorId);
        } else {
            log.debug("Bind rejected for session '{}' — no matching non-CLOSED/-ARCHIVED record with stale or free bind",
                    sessionId);
        }
        return bound;
    }

    /**
     * Same as {@link #tryBind} but unconditionally takes over the bind from
     * any other connection currently holding it. The caller MUST have
     * already verified that the requesting user owns the session
     * (matches {@link SessionDocument#getUserId()}). Used by the explicit
     * resume paths so a user re-opening a session from a fresh tab / pod
     * doesn't have to wait for the previous owner's heartbeat to go stale.
     * Different users still can't reach this method because the resume
     * handlers reject mismatched {@code userId} with a 403 before binding.
     *
     * <p>The previous owner's in-memory state on the other pod (connection
     * registry, WS handle) goes stale but does no harm — the next frame
     * from that connection gets rejected because Mongo's
     * {@code boundConnectionId} no longer matches.
     */
    public boolean tryBindWithUserTakeover(String sessionId, String editorId) {
        Instant now = Instant.now();
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_STATUS).nin(SessionStatus.CLOSED, SessionStatus.ARCHIVED));
        Update update = new Update()
                .set(F_BOUND_CONNECTION, editorId)
                .set(F_LAST_ACTIVITY, now);
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        boolean bound = result.getMatchedCount() == 1;
        if (bound) {
            log.debug("Bound session '{}' to editor '{}' via same-user takeover", sessionId, editorId);
        } else {
            log.debug("Bind rejected for session '{}' — no matching non-CLOSED/-ARCHIVED record",
                    sessionId);
        }
        return bound;
    }

    /**
     * Force-release the connection lock regardless of which connection
     * currently owns it. Used by Eddie's {@code MEDIATE} action when
     * the user explicitly asks to take over a session that another
     * connection (usually a stale tab) is sitting on. The session
     * itself stays OPEN; only the binding is cleared.
     *
     * <p>Multi-pod safe: the Mongo update is authoritative across
     * pods. The previous owner's in-memory state on the other pod
     * (connection registries, WS handle) goes stale but does no harm
     * — the next frame from that connection gets a "session lost"
     * rejection because Mongo's {@code boundConnectionId} no longer
     * matches.
     *
     * @return {@code true} when a bound connection was cleared,
     *         {@code false} when the session was already unbound or
     *         missing
     */
    public boolean forceUnbind(String sessionId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_BOUND_CONNECTION).ne(null));
        Update update = new Update()
                .set(F_BOUND_CONNECTION, null)
                .set(F_LAST_ACTIVITY, Instant.now());
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        boolean unbound = result.getModifiedCount() == 1;
        if (unbound) {
            log.info("Force-unbound session '{}'", sessionId);
        }
        return unbound;
    }

    /**
     * Releases the connection lock if this caller still owns it. Safe to call
     * on sessions that were already unbound — it just does nothing.
     */
    public void unbind(String sessionId, String editorId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_BOUND_CONNECTION).is(editorId));
        Update update = new Update()
                .set(F_BOUND_CONNECTION, null)
                .set(F_LAST_ACTIVITY, Instant.now());
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        if (result.getModifiedCount() == 1) {
            log.debug("Unbound session '{}' from editor '{}'", sessionId, editorId);
        }
    }

    /**
     * Hands the Mongo bind-lock atomically from {@code fromEditorId}
     * to {@code toEditorId}. Used by the multi-user disconnect path:
     * when the binding connection closes while other connections are
     * still attached, a survivor takes over so the session-lifecycle
     * (onDisconnect, idle-sweep) does not fire prematurely. See
     * {@code planning/multi-user-sessions.md} §3b.
     *
     * <p>Returns {@code true} when the bind was escalated;
     * {@code false} when the leaving editor no longer held the bind
     * (race with another disconnect, or the session was already
     * unbound).
     */
    public boolean tryEscalateBind(String sessionId, String fromEditorId, String toEditorId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_BOUND_CONNECTION).is(fromEditorId));
        Update update = new Update()
                .set(F_BOUND_CONNECTION, toEditorId)
                .set(F_LAST_ACTIVITY, Instant.now());
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        boolean escalated = result.getModifiedCount() == 1;
        if (escalated) {
            log.info("Escalated session '{}' bind from editor '{}' to '{}'",
                    sessionId, fromEditorId, toEditorId);
        }
        return escalated;
    }

    /**
     * Mass-release the {@code boundConnectionId} for every session whose
     * {@code projectId} is in {@code projectIds}. Used by
     * {@code ProjectLifecycleService.bring} when the project transitions
     * from non-RUNNING to RUNNING on this pod — any pre-existing bound
     * connection is guaranteed dead, immediate unbind avoids a 409 on
     * the next client reconnect.
     *
     * @return number of sessions that were released
     */
    public long unbindAllForProjects(java.util.Collection<String> projectIds) {
        if (projectIds == null || projectIds.isEmpty()) return 0;
        Query query = new Query(Criteria.where("projectId").in(projectIds)
                .and(F_BOUND_CONNECTION).ne(null));
        Update update = new Update().set(F_BOUND_CONNECTION, null);
        UpdateResult result = mongoTemplate.updateMulti(query, update, SessionDocument.class);
        long n = result.getModifiedCount();
        if (n > 0) {
            log.info("Reclaimed {} stale session binding(s) for {} project(s)",
                    n, projectIds.size());
        }
        return n;
    }

    /**
     * Cluster-wide stale-bind sweep. Releases {@code boundConnectionId}
     * on every session whose last heartbeat is older than {@code cutoff}
     * — i.e. the bound connection has missed too many pings to still be
     * considered live.
     *
     * <p>Driven by {@code SessionStaleBindSweepTick} on the cluster
     * master only, so a single sweep covers all pods. The query is
     * served by the {@code bound_activity_idx} partial index — only
     * bound sessions are visited, regardless of total session count.
     * No documents are loaded into memory; the {@code updateMulti}
     * executes server-side.
     *
     * <p>{@code lastActivityAt} is deliberately <em>not</em> touched —
     * the original timestamp documents when the connection actually
     * died, which is useful for diagnostics.
     *
     * @param cutoff sessions with {@code lastActivityAt < cutoff} are
     *     unbound — typically {@code now - bindStaleAfter}
     * @return number of sessions that were released
     */
    public long unbindStaleConnections(Instant cutoff) {
        Query query = new Query(Criteria.where(F_BOUND_CONNECTION).ne(null)
                .and(F_LAST_ACTIVITY).lt(cutoff));
        Update update = new Update().set(F_BOUND_CONNECTION, null);
        UpdateResult result = mongoTemplate.updateMulti(query, update, SessionDocument.class);
        long n = result.getModifiedCount();
        if (n > 0) {
            log.info("Stale-bind sweep released {} session binding(s) (cutoff={})", n, cutoff);
        }
        return n;
    }

    /**
     * Bumps {@link SessionDocument#getLastActivityAt()} — but only if this
     * connection still owns the lock. Returns {@code false} if the lock has
     * been lost (session closed, taken over, unbound) — the caller should
     * then drop the connection.
     */
    public boolean heartbeat(String sessionId, String editorId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_BOUND_CONNECTION).is(editorId));
        Update update = new Update().set(F_LAST_ACTIVITY, Instant.now());
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        return result.getModifiedCount() == 1;
    }

    /**
     * Stores the client-uploaded agent doc on the session. Idempotent —
     * a second upload overwrites the first. Pass {@code null} content to
     * clear (the corresponding fields are unset).
     */
    public void setClientAgentDoc(
            String sessionId, @Nullable String path, @Nullable String content) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId));
        Update update = new Update();
        if (content == null) {
            update.unset(F_CLIENT_AGENT_DOC).unset(F_CLIENT_AGENT_DOC_PATH);
        } else {
            update.set(F_CLIENT_AGENT_DOC, content)
                    .set(F_CLIENT_AGENT_DOC_PATH, path);
        }
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        if (result.getModifiedCount() == 1) {
            log.info("Stored client agent doc for session='{}' path='{}' chars={}",
                    sessionId, path, content == null ? 0 : content.length());
        }
    }

    /**
     * Atomically links a freshly-spawned chat-process to the session.
     * Succeeds only if the session currently has no
     * {@code chatProcessId} — protects against double-bootstrap on
     * a concurrent session-create + session-bootstrap race.
     *
     * @return {@code true} if the session was updated; {@code false}
     *         when the session is missing or already has a chat
     *         process linked
     */
    public boolean setChatProcessId(String sessionId, String chatProcessId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .andOperator(new Criteria().orOperator(
                        Criteria.where(F_CHAT_PROCESS_ID).isNull(),
                        Criteria.where(F_CHAT_PROCESS_ID).exists(false))));
        Update update = new Update().set(F_CHAT_PROCESS_ID, chatProcessId);
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        boolean ok = result.getModifiedCount() == 1;
        if (ok) {
            log.debug("Linked chatProcessId='{}' to session='{}'",
                    chatProcessId, sessionId);
        }
        return ok;
    }

    /**
     * Sets or clears {@code chatProcessId} on a session that already had
     * one — used by {@code reactivateFromArchive} to detach the old
     * (CLOSED) chat-process before {@code ensureChatProcess} spawns a
     * fresh one. Unconditional overwrite. Pass {@code null} to clear.
     * Callers must ensure the old process is already CLOSED.
     */
    public void replaceChatProcessId(String sessionId, @Nullable String newChatProcessId) {
        Update update;
        if (newChatProcessId == null) {
            update = new Update().unset(F_CHAT_PROCESS_ID);
        } else {
            update = new Update().set(F_CHAT_PROCESS_ID, newChatProcessId);
        }
        mongoTemplate.updateFirst(
                new Query(Criteria.where(F_SESSION_ID).is(sessionId)),
                update, SessionDocument.class);
    }

    /**
     * Updates the denormalised chat preview ({@code lastMessagePreview} +
     * {@code lastMessageAt} + {@code lastMessageRole}) on the session doc;
     * additionally seeds {@code firstUserMessage} on the very first
     * {@code USER}-role message and never overwrites it later. Two
     * conditional updates so a later USER message can't replace the
     * stable "topic". Idempotent against re-runs with the same content.
     *
     * <p>Called from {@link de.mhus.vance.shared.chat.ChatMessageService#append}
     * — keeps the chat hot-path adding at most two atomic Mongo ops.
     */
    public void touchChatPreview(
            String sessionId,
            @Nullable String role,
            @Nullable String content,
            @Nullable Instant at) {
        if (sessionId == null || sessionId.isBlank()) return;
        String preview = truncate(content);
        Instant when = at == null ? Instant.now() : at;

        Update lastUpdate = new Update()
                .set(F_LAST_MESSAGE_PREVIEW, preview)
                .set(F_LAST_MESSAGE_ROLE, role)
                .set(F_LAST_MESSAGE_AT, when);
        mongoTemplate.updateFirst(
                new Query(Criteria.where(F_SESSION_ID).is(sessionId)),
                lastUpdate,
                SessionDocument.class);

        if ("USER".equalsIgnoreCase(role) && preview != null) {
            // Set firstUserMessage only when the field is currently null /
            // missing — captures the session's "topic" once.
            Query firstQuery = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                    .andOperator(new Criteria().orOperator(
                            Criteria.where(F_FIRST_USER_MESSAGE).isNull(),
                            Criteria.where(F_FIRST_USER_MESSAGE).exists(false))));
            Update firstUpdate = new Update().set(F_FIRST_USER_MESSAGE, preview);
            mongoTemplate.updateFirst(firstQuery, firstUpdate, SessionDocument.class);
        }
    }

    private static @Nullable String truncate(@Nullable String content) {
        if (content == null) return null;
        // Collapse whitespace so multi-line messages stay readable in a
        // one-line preview slot.
        String flat = content.replaceAll("\\s+", " ").trim();
        if (flat.isEmpty()) return null;
        if (flat.length() <= PREVIEW_MAX_CHARS) return flat;
        return flat.substring(0, PREVIEW_MAX_CHARS - 1) + "…";
    }

