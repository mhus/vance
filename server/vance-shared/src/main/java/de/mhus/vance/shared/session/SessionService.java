    /**
     * Closes the session (marks {@link SessionStatus#CLOSED}, clears
     * binding, clears suspend-runtime fields). Idempotent.
     *
     * <p>Used by the close-cascade in {@code SessionLifecycleService}
     * <em>after</em> all of the session's processes have been stopped.
     */
    public void close(String sessionId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId));
        Update update = new Update()
                .set(F_STATUS, SessionStatus.CLOSED)
                .set(F_BOUND_CONNECTION, null)
                .set(F_LAST_ACTIVITY, Instant.now())
                .unset(F_SUSPENDED_AT)
                .unset(F_SUSPEND_CAUSE)
                .unset(F_TRANSITION_AT);
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        if (result.getModifiedCount() == 1) {
            log.info("Closed session '{}'", sessionId);
        }
    }

    /**
     * Moves a session into {@link SessionStatus#ARCHIVED}: stamps
     * {@code archivedAt}, clears the bind/suspend runtime fields. The
     * engine-stop cascade is the caller's responsibility (runs before
     * this call). Idempotent — re-archiving an already-ARCHIVED
     * session is a no-op.
     */
    public void archive(String sessionId) {
        SessionDocument session = repository.findBySessionId(sessionId).orElse(null);
        if (session == null) return;
        if (session.getStatus() == SessionStatus.ARCHIVED
                || session.getStatus() == SessionStatus.CLOSED) {
            return;
        }
        Instant now = Instant.now();
        Update update = new Update()
                .set(F_STATUS, SessionStatus.ARCHIVED)
                .set(F_ARCHIVED_AT, now)
                .set(F_BOUND_CONNECTION, null)
                .set(F_LAST_ACTIVITY, now)
                .unset(F_SUSPENDED_AT)
                .unset(F_SUSPEND_CAUSE)
                .unset(F_TRANSITION_AT);
        mongoTemplate.updateFirst(
                new Query(Criteria.where(F_SESSION_ID).is(sessionId)),
                update, SessionDocument.class);
        log.info("Archived session '{}'", sessionId);
    }

    /**
     * Marks an {@code ARCHIVED} session as live again: status →
     * {@link SessionStatus#IDLE}, clears {@code archivedAt}, stamps
     * {@code reactivatedAt}. Caller is responsible for spawning a
     * fresh engine instance and linking it via
     * {@link #replaceChatProcessId}.
     */
    public void reactivate(String sessionId) {
        Instant now = Instant.now();
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_STATUS).is(SessionStatus.ARCHIVED));
        Update update = new Update()
                .set(F_STATUS, SessionStatus.IDLE)
                .set(F_REACTIVATED_AT, now)
                .set(F_LAST_ACTIVITY, now)
                .unset(F_ARCHIVED_AT);
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        if (result.getModifiedCount() == 1) {
            log.info("Reactivated session '{}'", sessionId);
        }
    }

    /**
     * Marks the session SUSPENDED, stamping {@code suspendedAt},
     * {@code suspendCause}, {@code transitionAt}. {@code FORCED} is the
     * override case: {@code transitionAt} uses {@code forcedFloorMs}
     * regardless of {@code onSuspend} but still picks the right
     * <em>target</em> status (ARCHIVED for KEEP, CLOSED for CLOSE).
     *
     * <p>Idempotent — a second suspend on an already-suspended
     * session keeps the original timestamps. (We do not "refresh"
     * transitionAt on repeat suspends.)
     */
    public void suspend(String sessionId, SuspendCause cause, long forcedFloorMs) {
        SessionDocument session = repository.findBySessionId(sessionId).orElse(null);
        if (session == null) return;
        if (session.getStatus() == SessionStatus.SUSPENDED
                || session.getStatus() == SessionStatus.CLOSED
                || session.getStatus() == SessionStatus.ARCHIVED) {
            return;
        }
        Instant now = Instant.now();
        Instant transitionAt;
        if (cause == SuspendCause.FORCED) {
            transitionAt = now.plusMillis(forcedFloorMs);
        } else if (session.getOnSuspend() == SuspendPolicy.CLOSE) {
            transitionAt = now;
        } else {
            transitionAt = now.plusMillis(session.getSuspendKeepDurationMs());
        }
        Update update = new Update()
                .set(F_STATUS, SessionStatus.SUSPENDED)
                .set(F_SUSPENDED_AT, now)
                .set(F_SUSPEND_CAUSE, cause)
                .set(F_TRANSITION_AT, transitionAt)
                .set(F_LAST_ACTIVITY, now);
        mongoTemplate.updateFirst(
                new Query(Criteria.where(F_SESSION_ID).is(sessionId)),
                update, SessionDocument.class);
        log.info("Suspended session '{}' cause={} transitionAt={}",
                sessionId, cause, transitionAt);
    }

    /**
     * Resume a SUSPENDED session: status → IDLE, clear suspend-runtime fields.
     * Caller is responsible for resuming the engines.
     */
    public void resume(String sessionId) {
        Query query = new Query(Criteria.where(F_SESSION_ID).is(sessionId)
                .and(F_STATUS).is(SessionStatus.SUSPENDED));
        Update update = new Update()
                .set(F_STATUS, SessionStatus.IDLE)
                .set(F_LAST_ACTIVITY, Instant.now())
                .unset(F_SUSPENDED_AT)
                .unset(F_SUSPEND_CAUSE)
                .unset(F_TRANSITION_AT);
        UpdateResult result = mongoTemplate.updateFirst(query, update, SessionDocument.class);
        if (result.getModifiedCount() == 1) {
            log.info("Resumed session '{}'", sessionId);
        }
    }

    /**
     * Returns sessions in {@link SessionStatus#RUNNING}/{@link SessionStatus#IDLE}
     * configured with {@link IdlePolicy#SUSPEND} whose {@code lastActivityAt}
     * is older than {@code coarseCutoff}. This is the cheap pre-filter for
     * the idle-sweeper using the {@code status_activity_idx} index; the
     * caller must still verify each session's own {@code idleTimeoutMs}.
     * See {@code specification/session-lifecycle.md} §7.
     */
    public List<SessionDocument> findIdleCandidates(Instant coarseCutoff) {
        Query query = new Query(Criteria.where(F_STATUS)
                .in(SessionStatus.RUNNING, SessionStatus.IDLE)
                .and("onIdle").is(IdlePolicy.SUSPEND)
                .and(F_LAST_ACTIVITY).lt(coarseCutoff));
        return mongoTemplate.find(query, SessionDocument.class);
    }

    /**
     * Returns SUSPENDED sessions whose {@code transitionAt} has passed.
     * The sweep job dispatches them to archive/close cascade. Convenience
     * query — not part of the lifecycle code path.
     */
    public List<SessionDocument> findOverdueSuspended(Instant cutoff) {
        Query query = new Query(Criteria.where(F_STATUS).is(SessionStatus.SUSPENDED)
                .and(F_TRANSITION_AT).lte(cutoff));
        return mongoTemplate.find(query, SessionDocument.class);
    }

    /**
     * Returns SUSPENDED sessions whose {@code suspendCause} is
     * {@link SuspendCause#FORCED} and which this pod owns (via project).
     * Used by the auto-restart job on Brain boot.
     */
    public List<SessionDocument> findForcedSuspendedByProject(java.util.Collection<String> projectIds) {
        if (projectIds == null || projectIds.isEmpty()) return List.of();
        Query query = new Query(Criteria.where(F_STATUS).is(SessionStatus.SUSPENDED)
                .and(F_SUSPEND_CAUSE).is(SuspendCause.FORCED)
                .and("projectId").in(projectIds));
        return mongoTemplate.find(query, SessionDocument.class);
    }

    /** Physically drops the session record. For the hard-delete path. */
    public void delete(String sessionId) {
        repository.findBySessionId(sessionId).ifPresent(repository::delete);
    }

